////ini untuk inputan keyboard dari user
void KEYBOARD(unsigned char key, int x, int y) {
    ////escape key
    if (key == 27) {
        ////jika saat di scene tersebut dan ditekan esc key maka akan beralih scene
        if (scene == 6 || scene == 5 || scene == 3 || scene == 2 || scene == 1) {
            scene = 0;
        } else {
            scene = 4;
        }
    }

    if (scene == 0) {
        switch (key) {
            ////enter key
            case 13 :
                if (posisi == 1) {
                    scene = 1;
                } else if (posisi == 2) {
                    scene = 2;

                } else if (posisi == 3)
                    scene = 3;
                else
                    scene = 4;
                break;
        }
    }
}

