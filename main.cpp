////ini untuk logika controller keyboard
int posisi = 1;//pada awalnya memang posisi mulai adalah 1
void keyboard(int key, int x, int y) {
    if (scene == 1) {
        switch (key) {
            case GLUT_KEY_UP:
                if (ikankita[9] < 70) {
                    ikankita[5] += 1.5;
                }
                break;
            case GLUT_KEY_DOWN:
                if (ikankita[8] > -58) {
                    ikankita[5] -= 1.5;
                }
                break;
            case GLUT_KEY_LEFT:
                if (ikankita[6] > -171) {
                    ikankita[4] -= 1.5;
                    iskanan = 0;
                }
                break;
            case GLUT_KEY_RIGHT:
                if (ikankita[7] < 171) {
                    ikankita[4] += 1.5;
                    iskanan = 1;
                }
                break;
        }
    }
    if (scene == 0) {
        switch (key) {
            case GLUT_KEY_DOWN:
                if (posisi == 4)return;//jika posisi sudah mencapai 3, akan me return nilainya alias stuck
                else {
                    posisi++;//jika posisi belum mencapai 3 maka akan terus bisa ditambah
                }
                break;
            case GLUT_KEY_UP:
                if (posisi == 1)return;//jika posisi sudah mencapai 1, akan me return nilainya alias stuck
                else {
                    posisi--;//jika posisi belum mencapai 1 maka akan terus bisa ditambah
                }
                break;
        }
    }

    switch (posisi) {
        case 1://untuk memberi warna putih pilihan play
            a1 = 1, b1 = 1, c1 = 1;//putih
            a2 = 0, b2 = 0, c2 = 0;//hitam
            a3 = 0, b3 = 0, c3 = 0;//hitam
            a4 = 0, b4 = 0, c4 = 0;
            break;
        case 2://untuk memberi warna putih pilihan about
            a1 = 0, b1 = 0, c1 = 0;//hitam
            a2 = 1, b2 = 1, c2 = 1;//putih
            a3 = 0, b3 = 0, c3 = 0;//hitam
            a4 = 0, b4 = 0, c4 = 0;
            break;
        case 3://untuk memberi warna putih pilihan exit
            a1 = 0, b1 = 0, c1 = 0;//hitam
            a2 = 0, b2 = 0, c2 = 0;//hitam
            a3 = 1, b3 = 1, c3 = 1;//putih
            a4 = 0, b4 = 0, c4 = 0;
            break;
        case 4://untuk memberi warna putih pilihan exit
            a1 = 0, b1 = 0, c1 = 0;//hitam
            a2 = 0, b2 = 0, c2 = 0;//hitam
            a3 = 0, b3 = 0, c3 = 0;//putih
            a4 = 1, b4 = 1, c4 = 1;
            break;
    }
    glutPostRedisplay();
}

